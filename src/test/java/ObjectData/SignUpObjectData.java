package ObjectData;

public class SignUpObjectData {

    String newName;
    String newEmail;
    String existingName;
    String existingEmail;

    public String getNewName() {
        return newName;
    }

    public String getNewEmail() {
        return newEmail;
    }

    public String getExistingName() {
        return existingName;
    }

    public String getExistingEmail() {
        return existingEmail;
    }
}
