package ObjectData;

public class ContactUsObjectData {
    String name;
    String email;
    String subject;
    String message;
    String file;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getSubject() {
        return subject;
    }

    public String getMessage() {
        return message;
    }

    public String getFile() {
        return file;
    }
}
