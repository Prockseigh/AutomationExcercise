package ObjectData;

public class LogInObjectData {

    String emailAddr;
    String password;
    String invalidEmail;
    String invalidPassword;

    public String getEmailAddr() {
        return emailAddr;
    }

    public String getPassword() {
        return password;
    }

    public String getInvalidEmail() {
        return invalidEmail;
    }

    public String getInvalidPassword() {
        return invalidPassword;
    }
}
